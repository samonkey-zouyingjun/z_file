package com.zidoo.custom.animation;

public class ZidooPosition {
    public float mFromX;
    public float mFromY;
    public float mToX;
    public float mToY;

    public ZidooPosition(float fromX, float fromY, float toX, float toY) {
        this.mFromX = fromX;
        this.mFromY = fromY;
        this.mToX = toX;
        this.mToY = toY;
    }
}
